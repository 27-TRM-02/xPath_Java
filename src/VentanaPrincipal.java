    private void ConsultaXPathActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultaXPathActionPerformed
        
        if (instruccionesTextArea.getText().equals("")){
            ResultadoLabel.setText("Escribe consulta XPath");
        } else {
            String res = metodosXPath.EjecutaXPath(instruccionesTextArea.getText());
            infoTextArea.setText(res);
            ResultadoLabel.setText("");
        }
    }//GEN-LAST:event_ConsultaXPathActionPerformed

    /**
     * @return File seleccionado por el usuario 
     */
    private File dialogoSeleccionFichero() {
        File f = null;
        JFileChooser jfc = new JFileChooser();
        jfc.setMultiSelectionEnabled(false);
        jfc.setDialogType(JFileChooser.OPEN_DIALOG);
        // Guarda con que se ha cerrado el file chooser
        int res = jfc.showOpenDialog(this);
        if (res == JFileChooser.APPROVE_OPTION) {
            f = jfc.getSelectedFile();
        }
        return f;
    }

